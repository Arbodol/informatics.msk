import java.util.Scanner;

/**Король
 Ввод и вывод данных производятся через стандартные потоки ввода-вывода.
 Поле шахматной доски определяется парой чисел (a, b), каждое от 1 до 8, первое число задает номер столбца, второе – номер
 строки. Заданы две клетки. Определите, может ли шахматный король попасть с первой клетки на вторую за один ход.

 Входные данные
 Даны 4 целых числа от 1 до 8 каждое, первые два задают начальную клетку, вторые два задают конечную клетку. Начальная и
 конечная клетки не совпадают. Числа записаны в отдельных строках.

 Выходные данные
 Программа должна вывести YES, если из первой клетки ходом короля можно попасть во вторую, или NO в противном случае.

 Примеры
 входные данные
 4
 4
 5
 5
 выходные данные
 YES
 */

public class ChessKing {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int x1 = scn.nextInt();
        int y1 = scn.nextInt();
        int x2 = scn.nextInt();
        int y2 = scn.nextInt();
        if (Math.abs(x1 - x2) >= 1 && Math.abs(y1 - y2) >= 1){
            System.out.println("YES");
        }else System.out.println("NO");
        /** Пробовал и таким способом, но сайт все равно выдает "Частичное решение"
         * if (x1 == x2 - 1 && y1 == y2 - 1){
         * System.out.println("YES");
         * }else System.out.println("NO");
         */
    }
}
